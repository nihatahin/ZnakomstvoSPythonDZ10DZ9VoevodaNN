#----------------------------------------------------------------------------
#----------IMPORTS-----------------------------------------------------------
#----------------------------------------------------------------------------
import time
#----------------------------------------------------------------------------
#----------VARIABLES---------------------------------------------------------
#----------------------------------------------------------------------------
log_path = './task2d1/files/log_file.txt'
#----------------------------------------------------------------------------
#----------FUNCTIONS---------------------------------------------------------
#----------------------------------------------------------------------------
def tel_log(name, id, msg):
    file = open(get_log_path(), 'a')
    file.write(f"{time_str()} - {name}: {id} - {msg}\n")
    file.close()
#----------------------------------------------------------------------------
def get_log_path():
    return log_path
#----------------------------------------------------------------------------
def time_str():
    result = time.localtime(time.time())
    return f"[{result.tm_year}.{str(result.tm_mon).rjust(2, '0')}.{str(result.tm_mday).rjust(2, '0')}:{str(result.tm_hour).rjust(2, '0')}:{str(result.tm_min).rjust(2, '0')}:{str(result.tm_sec).rjust(2, '0')}]"
