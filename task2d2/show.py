def show_rec(message, print_lst):
    if len(print_lst) < 1:
        bot.send_message(chat_id= message.from_user.id, text= "There is nobody!")    
    else:
        for i in range(len(print_lst)):
            fin_str = str(i+1) + f".\n    Name:\t\t{print_lst[i][0]}\n" + f"    Surename:\t{print_lst[i][1]}\n" + f"    Phone number:\t{tel2str(print_lst[i][2])}\n" + f"    Description:\t{print_lst[i][3]}"
            bot.send_message(chat_id= message.from_user.id, text= fin_str)
